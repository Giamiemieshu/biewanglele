    /**
     * 监听输入框，自动选中匹配的 Emoji
     * @param {HTMLInputElement} inputElement - 物品名称输入框
     * @param {HTMLElement} pickerElement - 图标选择器容器
     */
    watch(inputElement, pickerElement) {
      if (!inputElement || !pickerElement) return;

      let lastMatch = null;

      inputElement.addEventListener('input', () => {
        const text = inputElement.value.trim();
        const matched = matchBest(text);

        if (matched && matched !== lastMatch) {
          const options = pickerElement.querySelectorAll('.icon-option');
          for (const opt of options) {
            if (opt.dataset.emoji === matched) {
              options.forEach(o => o.classList.remove('selected'));
              opt.classList.add('selected');
              // 滚动到可见区域
              opt.scrollIntoView({ block: 'nearest', behavior: 'smooth' });
              lastMatch = matched;
              break;
            }
          }
        } else if (!matched) {
          lastMatch = null;
        }
      });
    },

    /**
     * 手动匹配（供其他场景使用）
     */
    match(text) {
      return matchBest(text);
    },
