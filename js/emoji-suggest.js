  // ==================== 匹配逻辑 ====================

  function matchBest(text) {
    const t = text.trim();
    if (!t) return null;

    let bestMatch = null;
    let bestLen = 0;

    for (const [keywords, emoji] of KEYWORD_MAP) {
      for (const kw of keywords) {
        if (t.includes(kw)) {
          // 优先匹配更长的关键词（更精确）
          if (kw.length > bestLen) {
            bestLen = kw.length;
            bestMatch = emoji;
          }
        }
      }
    }

    return bestMatch;
  }

